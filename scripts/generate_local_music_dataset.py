"""Create a copyright-safe local music dataset for the EmotiCare demo.

The generated tracks are short procedural soundscapes, not copies of FMA audio.
FMA metadata remains in ``media-dataset/source`` for future research work.
"""

from __future__ import annotations

import json
import subprocess
from pathlib import Path


ROOT = Path(__file__).resolve().parent.parent
TARGET = ROOT / "media-dataset" / "music"
CATALOG = ROOT / "media-dataset" / "music_catalog.json"

TRACKS = [
    ("Calm Horizon", "relax", "sine=frequency=174:sample_rate=44100,volume=0.08"),
    ("Soft Rain Pulse", "relax", "anoisesrc=color=pink:sample_rate=44100,volume=0.04"),
    ("Deep Work Tone", "focus", "sine=frequency=220:sample_rate=44100,volume=0.05"),
    ("Clear Study Loop", "focus", "sine=frequency=330:sample_rate=44100,volume=0.04"),
    ("Night Drift", "sleep", "sine=frequency=110:sample_rate=44100,volume=0.035"),
    ("Bright Steps", "happy", "sine=frequency=440:sample_rate=44100,volume=0.05"),
    ("Warm Morning", "happy", "sine=frequency=523:sample_rate=44100,volume=0.04"),
    ("Gentle Grounding", "sad_support", "sine=frequency=196:sample_rate=44100,volume=0.05"),
    ("Release Tension", "anger_release", "anoisesrc=color=brown:sample_rate=44100,volume=0.035"),
    ("Energy Reset", "energy_recover", "sine=frequency=392:sample_rate=44100,volume=0.05"),
]


def main() -> None:
    TARGET.mkdir(parents=True, exist_ok=True)
    catalog = []
    for index, (title, category, source) in enumerate(TRACKS, start=1):
        filename = f"{index:02d}-emoticare-soundscape.mp3"
        destination = TARGET / filename
        subprocess.run(
            ["ffmpeg", "-y", "-f", "lavfi", "-i", source, "-t", "30", "-codec:a", "libmp3lame", "-b:a", "128k", str(destination)],
            check=True,
            capture_output=True,
        )
        catalog.append({
            "media_type": "song", "title": title, "creator": "EmotiCare Local Soundscapes",
            "category": category, "duration_sec": 30, "source_url": f"/media/music/{filename}",
            "dataset": "EmotiCare Local Music Dataset v1", "license_note": "Procedurally generated by the project; no external URL stored.",
        })
    CATALOG.write_text(json.dumps(catalog, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Generated {len(catalog)} local music tracks.")


if __name__ == "__main__":
    main()
